// Включаем стандартный заголовочный файл для ввода/вывода
#include <iostream>

// Используем namespace std для避免 повторного использования std::
using namespace std;

int main() 
{ 
    // Объявляем переменные для хранения размера массива, текущего значения и промежуточного значения
    int array_size, value, intermediate_value;

    // Читаем размер массива из стандартного ввода
    cin >> array_size;

    // Инициализируем промежуточное значение в 0 (хотя оно не используется в данном коде)
    intermediate_value = 0;

    // Выделяем динамическую память для массива размером array_size
    int *array = new int[array_size];

    // Цикл для заполнения массива и вывода суммированных значений
    for (int i = 0; i < array_size; i++) 
    {
        // Читаем текущее значение из стандартного ввода
        cin >> value;

        // Если введенное значение равно 0, прерываем цикл
        if (value == 0) 
        {
            break;
        }

        // Если это первый элемент массива, просто присваиваем ему введенное значение
        if (i == 0) 
        {
            array[i] = value;
        }

        // Для остальных элементов массива: суммируем текущее значение с предыдущим элементом массива
        if (i >= 1) 
        {
            // Суммируем текущее значение с предыдущим элементом и сохраняем результат в текущем элементе
            array[i] = array[i - 1] + value;
            
            // Выводим сумму на экран
            cout << array[i] << "\n";
        }
    }  

    // Освобождаем динамически выделенную память для массива
    delete[] array;      
}

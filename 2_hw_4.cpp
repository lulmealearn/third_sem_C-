// Включаем стандартный заголовочный файл для ввода/вывода
#include <iostream>
// Включаем стандартный заголовочный файл для работы со строками
#include <string>

// Используем namespace std для避免 повторного использования std::
using namespace std;

int main() 
{ 
    // Объявляем переменные для хранения двух строк и счетчика вхождений подстроки
    string str_1, str_2;
    int count = 0, len_1 = 0, len_2 = 0, substring;

    // Читаем две строки из стандартного ввода
    cin >> str_1;
    cin >> str_2;

    // Определяем длины обеих строк
    len_1 = str_1.length();
    len_2 = str_2.length();

    // Цикл для поиска всех вхождений подстроки str_2 в строке str_1
    for (int i = 0; i < (len_1 - len_2 + 1); ) // Исправлено условие цикла
    {
        // Ищем подстроку str_2 в строке str_1, начиная с позиции i
        substring = str_1.find(str_2, i);

        // Если подстрока не найдена, прерываем цикл
        if (substring == string::npos)
        {
            break;
        }

        // Увеличиваем счетчик найденных вхождений
        ++count;

        // Переходим к следующей позиции после найденной подстроки
        i = substring + len_2; // Исправлено смещение
    }

    // Выводим общее количество найденных вхождений подстроки
    cout << count;
}
